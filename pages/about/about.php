			The center is based in Kenya - Africa, on the grounds of the headquarters of the Orthodox Archbishopric of Kenya. The Orthodox Teachers College of Africa opened its doors to its first class of students in January 2003. The College is the first of its kind in Orthodox Africa. It is a tertiary teacher training institution providing post-secondary education to students’ region-wide. It is recognized and registered with the Ministry of Education, Science and Technology of Kenya and offers young East Africans the opportunity to study in a tertiary institution of higher learning.

		</p>
		<p>
			Orthodox Teachers Training College of Africa is located in the Dagoretti area of Nairobi and offers various courses in ECDE, social work and community development, information technology as well as accounting and tailoring. This higher learning institution key aim is to be a center of academic excellence in teaching, learning and research based on religious principles. The institution’s mission is to provide training, research and participate in national educational planning policy. They provide students with courses across a myriad of programmes such as Diploma in Education (Arts & Science) Diploma in Education(Arts), Diploma in Education (Science), Diploma in Education and Counselling (Education and Counseling), Diploma in Education, Guidance & Counselling (Education, Guidance & Counseling), Diploma in Education Planning (Education Planning), Diploma in Education(arts), Diploma in Educational Administration (Educational Administration), Accounting Technician Certificate (ATC), Certificate in Early Childhood Development and Education (ECDE), Certificate in Information Technology, Certificate in Social Work and Community Development, Certified Public Accountant (CPA).
