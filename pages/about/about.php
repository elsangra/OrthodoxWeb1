		<p>
			Orthodox Teachers Training College of Africa’s faculty is composed of international university graduates in the field of education, including holders of doctoral degrees. The Rector/Principal of the College is His Eminence Metropolitan Makarios, who holds a doctorate from the University of Oxford and post-doctoral qualifications from the University of Louvain. The senior lecturers include in the area of child linguistics, and Archimandrite Johannes Eko, who is an experienced missionary of the Finnish Orthodox Church and an expert in the area of language instruction. In addition, several Kenyan university graduates who are specialists in early childhood education are members of the faculty.
		</p>
